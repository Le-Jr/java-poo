package Camiseta;

/**
 * Camiseta
 */
public class Camiseta {
    private String tecido;
    private char tamanho;

    public void setTecido(String tecido) {
        this.tecido = tecido;
    }

    public String getTecido() {
        return this.tecido;
    }

    public void setTamanho(char tamanho) {
        this.tamanho = tamanho;
    }

    public char getTamanho() {
        return this.tamanho;
    }

}
